internal class Program
{
    private static void Main(string[] args)
    {
        int a = 4, b = 3; // ввод целочисленных переменных a,b
        double c = Math.Sqrt(a * a + b * b); // математическая формула для вычисления гипотенузы
        Console.WriteLine("Гипотенуза = " + c + ""); // вывод на экран строки "..."
    }
}